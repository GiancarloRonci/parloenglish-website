function getLang() {
  return localStorage.getItem('pe_lang') || 'it';
}

function setLang(lang) {
  localStorage.setItem('pe_lang', lang);
  applyLang(lang);
}

function applyLang(lang) {
  const t = T[lang];
  if (!t) return;

  document.documentElement.lang = lang;

  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.dataset.i18n;
    if (t[key] !== undefined) el.textContent = t[key];
  });

  document.querySelectorAll('[data-i18n-html]').forEach(el => {
    const key = el.dataset.i18nHtml;
    if (t[key] !== undefined) el.innerHTML = t[key];
  });

  document.querySelectorAll('[data-i18n-alt]').forEach(el => {
    const key = el.dataset.i18nAlt;
    if (t[key] !== undefined) el.alt = t[key];
  });

  document.querySelectorAll('[data-i18n-src]').forEach(el => {
    const key = el.dataset.i18nSrc;
    if (t[key] !== undefined) el.src = t[key];
  });

  const titleKey = document.documentElement.dataset.titleKey;
  if (titleKey && t[titleKey]) document.title = t[titleKey];

  const metaKey = document.documentElement.dataset.metaKey;
  const meta = document.querySelector('meta[name="description"]');
  if (metaKey && meta && t[metaKey]) meta.setAttribute('content', t[metaKey]);

  // lang-section visibility (privacy policy)
  document.querySelectorAll('.lang-section').forEach(el => {
    el.classList.toggle('active', el.id === 'lang-' + lang);
  });

  // lang button active state
  document.querySelectorAll('.lang-btn[data-lang]').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.lang === lang);
  });
}

document.addEventListener('DOMContentLoaded', () => applyLang(getLang()));
